namespace TSN\ProductQuestionnaire\Controller\Adminhtml\Question;

use TSN\ProductQuestionnaire\Controller\Adminhtml\Question;

class Add extends Question
{
    /**
     * Forward to edit
     *
     * @return \Magento\Backend\Model\View\Result\Forward
     */
